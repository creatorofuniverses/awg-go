package icons

import (
	"hash/fnv"
	"image/color"
)

// Palette is 12 colours chosen for distinguishability on light and dark panels.
var Palette = []color.RGBA{
	{0x3b, 0x82, 0xf6, 0xff}, // blue-500
	{0xef, 0x44, 0x44, 0xff}, // red-500
	{0x10, 0xb9, 0x81, 0xff}, // emerald-500
	{0xf5, 0x9e, 0x0b, 0xff}, // amber-500
	{0x8b, 0x5c, 0xf6, 0xff}, // violet-500
	{0xec, 0x48, 0x99, 0xff}, // pink-500
	{0x06, 0xb6, 0xd4, 0xff}, // cyan-500
	{0x84, 0xcc, 0x16, 0xff}, // lime-500
	{0xf9, 0x73, 0x16, 0xff}, // orange-500
	{0x64, 0x74, 0x8b, 0xff}, // slate-500
	{0xd9, 0x46, 0xef, 0xff}, // fuchsia-500
	{0x14, 0xb8, 0xa6, 0xff}, // teal-500
}

func ColourFromName(name string) color.RGBA {
	h := fnv.New32a()
	_, _ = h.Write([]byte(name))
	return Palette[h.Sum32()%uint32(len(Palette))]
}
