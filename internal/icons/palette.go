func ParseFlavour(name string) (Flavour, bool) {
	switch strings.ToLower(strings.TrimSpace(name)) {
	case "mocha":
		return FlavourMocha, true
	case "latte":
		return FlavourLatte, true
	case "frappe":
		return FlavourFrappe, true
	case "macchiato":
		return FlavourMacchiato, true
	default:
		return FlavourMocha, false
	}
}

func ColourFromName(name string, palette []color.RGBA) color.RGBA {
	h := fnv.New32a()
	_, _ = h.Write([]byte(name))
	return palette[h.Sum32()%uint32(len(palette))]
}
